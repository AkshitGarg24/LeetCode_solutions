class Solution
{
    public:
    int carry(Node *head){
        if(head==NULL){
            return 1;
        }
        int x = carry(head->next);
        head->data += x;
        if(head->data<10) return 0;
        head->data = 0;
        return 1;
        
    }
    Node* addOne(Node *head) 
    {
        int x = carry(head);
        if(x==1){
            Node *ans = new Node(1);
            ans->next = head;
            head = ans;
        }
        return  head;
    }
};

